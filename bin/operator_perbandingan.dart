//operator perbandingan => menghasilkan tipe BOOLEAN/bool
// digunakan untuk tipe data number dan string
// (==) => sama dengan
// (!=) => tidak sama dengan
// (<) => kurang dari
// (>) => lebih dari
// (<=) => kurang dari sama dengan
// (>=) => lebih dari sama dengan

void main() {

  print(10 == 10);
  print(10 != 10);
  print(10 < 10);
  print(10 > 10);
  print(10 <= 10);
  print(10 >= 10);
}