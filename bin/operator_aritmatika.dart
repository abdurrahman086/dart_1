// + => pertambahan
// - => pengurangan
// * => perkalian
// / => pembagian, hasil double
// % => modulus/sisa bagi
// ~/ => floor division (pembagian, hasil int)

void main() {
  var pertama = 17;
  var kedua = 3;

  print(pertama + kedua);
  print(pertama - kedua);
  print(pertama * kedua);
  print(pertama / kedua);
  print(pertama ~/ kedua);
  print(pertama % kedua);
}